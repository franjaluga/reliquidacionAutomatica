public class DataYear {
    int [] tramos = new int[7];
    float [] tasas = new float[8];
    double [] rebajas = new double[8];

    public int[] getTramos() {
        return tramos;
    }

    public void setTramos(int[] tramos) {
        this.tramos = tramos;
    }

    public float[] getTasas() {
        return tasas;
    }

    public void setTasas(float[] tasas) {
        this.tasas = tasas;
    }

    public double[] getRebajas() {
        return rebajas;
    }

    public void setRebajas(double[] rebajas) {
        this.rebajas = rebajas;
    }
}
